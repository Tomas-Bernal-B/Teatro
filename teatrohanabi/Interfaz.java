    public void setMenuOptions(String menu){
        JPReportes.setVisible(menu.equals("Reportes"));
        JPBoletos.setVisible(menu.equals("Boletos"));
        JPUsuarios.setVisible(menu.equals("Usuarios"));
        JPObras.setVisible(menu.equals("Obras"));
        
        JLTitulo.setText(menu);
        
        if(menu.equals("Usuarios")){
            setMenuUsuariosOptions("Lista de usuarios");
        }else if(menu.equals("Boletos")){
            try{
                ObraTeatro ListTeatro = new ObraTeatro();
                int auxCount = 0;
                ArrayList<ObraTeatro> AuxListTeatro = ListTeatro.ListObraTeatro();
                String[] ModelTeatro = new String[AuxListTeatro.size() + 1];
                ModelTeatro[auxCount++] = "Selecciona una opcion";
                for ( ObraTeatro Teatro : AuxListTeatro){
                    ModelTeatro[auxCount++] = Teatro.get_Nombre();
                }
                DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>( ModelTeatro );
                JCBoletosObra.setModel( model );
            }catch(Exception e){
                System.out.println(e);
            }
        }
    }
    
    public void setMenuUsuariosOptions(String menu){
        UsuariosContentAgregar.setVisible(menu.equals("Agregar usuario"));
        UsuariosContentLista.setVisible(menu.equals("Lista de usuarios"));
        JLUsuariosTitulo.setText(menu);

        if(menu.equals("Agregar usuario")){
            JBUsuariosOption.setLabel("Lista");
        }else if(menu.equals("Lista de usuarios")){
            Usuarios usu = new Usuarios();
            try{
                ArrayList<Usuarios> ListaUsuarios = usu.ListUsuarios();
                global.removeAllTable(JTListaUsuarios);
                for(Usuarios usuario: ListaUsuarios)
                    global.addElementTable(JTListaUsuarios, new Object[]{
                        usuario.get_usuario(),
                        usuario.get_nombre(),
                        usuario.get_puesto(),
                        usuario.get_estatus()? "Activo": "No activo"
                    });
            }catch(Exception e){
                System.out.println(e);
            }
            
            JBUsuariosOption.setLabel("Agregar");
        }
    }
