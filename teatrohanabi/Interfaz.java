    private void JCBoletosObraItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_JCBoletosObraItemStateChanged
        String ObraSelect = (String)JCBoletosObra.getSelectedItem();
        try{
            ObraTeatro ListTeatro = new ObraTeatro(ObraSelect);
            VentaBoletos Boletos = new VentaBoletos();
            int countAux = 0;
            ListTeatro.SearchObras();
            String[] ModelTeatro = new String[ListTeatro.get_Num_Lugares()];
            ModelTeatro[0] = "Selecciona una opcion";
            for (int i = 1; i < ListTeatro.get_Num_Lugares(); i++){
                if(!Boletos.validAsiento("A"+i, ObraSelect))
                    ModelTeatro[countAux++] = "A"+i;
                
            }
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>( ModelTeatro );
            JCBoletosNAsientos.setEnabled(true);
            JCBoletosNAsientos.setModel( model );
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_JCBoletosObraItemStateChanged

